void Traverse(f2elm_t r, int j, int k, int z, const int *P, const f2elm_t **T, 
              int *D, int Dlen, int ell, PCurveIsogenyStruct CurveIsogeny)
{
    f2elm_t rp = {0}, s = {0};
    
    if (z > 1) {
        int t = P[z];
        fp2copy751(r, rp);
        for (int i = 0; i < z-t; i++) {
            if (ell == 2)
                sqr_Fp2_cycl(rp, CurveIsogeny->Montgomery_one);
            else
                cube_Fp2_cycl(rp, CurveIsogeny->Montgomery_one);
        }
        Traverse(rp, j + (z - t), k, t, P, T, D, Dlen, ell, CurveIsogeny);  
        
        fp2copy751(r, rp);
        for (int h = k; h < k + t; h++)
            fp2mul751_mont(rp, T[j + h][D[h]], rp);
        
        Traverse(rp, j, k + t, z - t, P, T, D, Dlen, ell, CurveIsogeny);
    } else {
        for (int t = 0; t < ell; t++) {         
            fp2_conj(T[Dlen - 1][t], s);
            fp2correction751(s);
            fp2correction751(r);
            if (fp2compare751(s,r) == 0) {
                D[k] = t;
                break;
            }
        }
    }
}

void Precomp(f2elm_t invg, f2elm_t **T, int ell, int e, PCurveIsogenyStruct CurveIsogeny) 
{   
    felm_t zero = {0};
    
    for (int i = 0; i < e; i++) {     
        for (int j = 0; j < ell; j++) {
            fpcopy751(zero, T[i][j][1]);
            fpcopy751(CurveIsogeny->Montgomery_one, T[i][j][0]);
        }
    }
    for (int d = 1; d < ell; d++)
        fp2mul751_mont(invg, T[0][d - 1], T[0][d]);
    for (int u = 1; u < e; u++) {
        for (int d = 1; d < ell; d++) {
            fp2copy751(T[u - 1][d], T[u][d]);
            if (ell == 2) 
                sqr_Fp2_cycl(T[u][d], CurveIsogeny->Montgomery_one);
            else
                cube_Fp2_cycl(T[u][d], CurveIsogeny->Montgomery_one);
        }
    }    
    for (int i = 0; i < e; i++)
        for (int j = 0; j < ell; j++)
            fp2correction751(T[i][j]);
}